Мы оценим ваше решение на основе следующих критериев:
1 Полнота: Соответствует ли ваше решение указанным выше требованиям?
2 Качество кода: Является ли ваш код хорошо структурированным, модульным и поддерживаемым? Соответствует ли он конвенциям и лучшим практикам Swift?
3 Пользовательский интерфейс: Является ли интерфейс вашего приложения интуитивно понятным, легким в использовании и визуально привлекательным?
4 Сеть: Обрабатывает ли ваше приложение ошибки сети грамотно?
