Описание задачи:

Ваша задача состоит в создании простого приложения, которое получает и отображает список книг из Open Library и позволяет пользователю просмотреть подробности о каждой книге.

Требования:

1 Приложение должно иметь главный экран, на котором отображается список книг, полученных из JSON API. Каждая книга должна отображать свой заголовок, дату первой публикации и обложку.
2 При нажатии на книгу в списке пользователь должен перейти на экран с подробной информацией о книге, включающей заголовок, описание, дату первой публикации, обложку и среднюю оценку пользователей.
3 Приложение должно иметь чистый и интуитивно понятный пользовательский интерфейс, соответствующий рекомендациям по человеко-машинному взаимодействию компании Apple.

Ограничения:

1 Приложение должно быть разработано на Swift и последней версии Xcode.
2 Приложение должно работать на iOS 13 и выше.
 
